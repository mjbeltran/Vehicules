package com.sqli.domain;

public class Vehicle {

	private String vehicleType;
	private String vehicleFuel;
	private int vehicleDoors;
	
	
	public String getVehicleType() {
		return vehicleType;
	}
	public void setVehicleType(String vehicleType) {
		this.vehicleType = vehicleType;
	}
	public String getVehicleFuel() {
		return vehicleFuel;
	}
	public void setVehicleFuel(String vehicleFuel) {
		this.vehicleFuel = vehicleFuel;
	}
	public int getVehicleDoors() {
		return vehicleDoors;
	}
	public void setVehicleDoors(int vehicleDoors) {
		this.vehicleDoors = vehicleDoors;
	}
	
}
